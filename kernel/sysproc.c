uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_trace(void)
{
  struct proc *p = myproc();

  // retrieve the trace mask
  if (argint(0, &p->trace_mask) < 0)
    return -1;

  return 0;
}

uint64
sys_sysinfo(void)
{
  // get current process running on cpu
  struct proc *p = myproc();
  // temporary buffer to store sysinfo in kernel space
  struct sysinfo info;
  // user space pointer to struct sysinfo
  uint64 pinfo;

  // get user space pointer
  if (argaddr(0, &pinfo) < 0)
    return -1;

  // get sysinfo
  info.freemem = nfreemem();
  info.nproc = nproc();

  // copy sysinfo from kernel to user
  if (copyout(p->pagetable, pinfo, (char *)&info, sizeof(struct sysinfo)) < 0)
    return -1;

  return 0;
}
